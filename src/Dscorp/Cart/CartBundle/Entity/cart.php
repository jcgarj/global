<?php

namespace Dscorp\Cart\CartBundle\Entity;

/**
 * cart
 */
class cart
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $estatus;

    /**
     * @var string
     */
    private $llave;

    /**
     * @var \DateTime
     */
    private $dateCart;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set estatus
     *
     * @param string $estatus
     *
     * @return cart
     */
    public function setEstatus($estatus)
    {
        $this->estatus = $estatus;

        return $this;
    }

    /**
     * Get estatus
     *
     * @return string
     */
    public function getEstatus()
    {
        return $this->estatus;
    }

    /**
     * Set llave
     *
     * @param string $llave
     *
     * @return cart
     */
    public function setLlave($llave)
    {
        $this->llave = $llave;

        return $this;
    }

    /**
     * Get llave
     *
     * @return string
     */
    public function getLlave()
    {
        return $this->llave;
    }

    /**
     * Set dateCart
     *
     * @param \DateTime $dateCart
     *
     * @return cart
     */
    public function setDateCart($dateCart)
    {
        $this->dateCart = $dateCart;

        return $this;
    }

    /**
     * Get dateCart
     *
     * @return \DateTime
     */
    public function getDateCart()
    {
        return $this->dateCart;
    }
}
